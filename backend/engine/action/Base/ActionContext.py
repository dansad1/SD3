class ActionContext:

    def __init__(
        self,
        *,
        action,
        request,
        payload=None,
        ctx=None,
    ):

        # =================================================
        # CORE
        # =================================================

        self.action = action

        self.request = request

        # =================================================
        # INPUT
        # =================================================

        self.payload = (
            payload or {}
        )

        # parent entity/list/form context
        self.ctx = (
            ctx or {}
        )

        # =================================================
        # RUNTIME FIELDS
        # =================================================

        self.runtime_fields = []

        self.field_map = {}

        # =================================================
        # SCHEMA
        # =================================================

        self.fields = []

        self.schema = {}

        # =================================================
        # DATA
        # =================================================

        # normalized payload
        self.data = {}

        # =================================================
        # RESULT
        # =================================================

        self.result = {}

        # =================================================
        # ERRORS
        # =================================================

        self.errors = {}